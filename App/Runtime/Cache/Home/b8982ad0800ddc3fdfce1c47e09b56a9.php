					$('.purchase ul').eq(0).find('li').eq(indexA).remove();
					$('.unwoundTip').fadeOut();
				});
				$('.purchase .delete').click(function(){
					indexB = $(this).parent('.handle').parent('li').index();
					$('.deleteTip').fadeIn();
				});
